function removeDup(arr) {
  let seen = {};

  let unique = [];

  for (let i = 0; i < arr.length; i++) {
    arr[i].sort((a, b) => a - b);
    if (!seen[JSON.stringify(arr[i])]) {
      unique.push(arr[i]);
      seen[JSON.stringify(arr[i])] = true;
    }
  }
  return unique;
}

function pushOnes(arr, cnt) {
  while (cnt > 0) {
    arr.push(1);
    cnt--;
  }
  return arr;
}

function normaliser(arr) {
  for (let i = 0; i < arr.length; i++) {
    pushOnes(arr[i], i);
  }
  return arr;
}

function sums(num, parts = [], add = 1) {
  let sub = num - add;

  if (add === num) {
    return parts;
  }
  while (sub > 0) {
    let part = [sub];

    let n = sub;
    while (n < num) {
      part.splice(0, 0, add);
      n += add;
    }
    sub--;

    parts.push(part);
  }

  return sums(num, parts, add + 1);
}

